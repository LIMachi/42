#include <libft.h>

double	ft_modd(double v, double d)
{
	if (d == 0.0)
		return (-1.0);
	if (v == 0)
		return (0.0);
	if (d < 0.0)
		d = -d;
	while (v < 0.0)
		v += d;
	while (v >= d)
		v -= d;
	return (v);
}
