#include <stddef.h>

size_t	ft_evaluate_i128_size(__int128_t v)
{
	size_t		l;
	__uint128_t	t;

	if (v == 0)
		return (1);
	if (v < 0)
	{
		l = 2;
		t = -v;
	}
	else
	{
		l = 1;
		t = v;
	}
	while (t /= 10)
		++l;
	return (l);
}
