#include <libft.h>

char	*ft_strrchr(const char *str, int c)
{
	char	*p_tmp;

	p_tmp = (char *)str;
	while (*p_tmp)
		++p_tmp;
	while (p_tmp >= (char *)str)
		if (*p_tmp-- == c)
			return (p_tmp + 1);
	return (NULL);
}
