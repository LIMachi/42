#include <libft.h>

float	ft_modf(float v, float d)
{
	if (d == 0.0f)
		return (-1.0f);
	if (v == 0)
		return (0.0f);
	if (d < 0.0f)
		d = -d;
	while (v < 0.0f)
		v += d;
	while (v >= d)
		v -= d;
	return (v);
}
