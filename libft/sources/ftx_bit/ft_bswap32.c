#include <libft.h>

unsigned long int	ft_bswap32(const unsigned long int x)
{
	return (((x & 0xFF000000) >> 24) | ((x & 0xFF0000) >> 8)
			| ((x & 0xFF00) << 8) | ((x & 0xFF) << 24));
}
