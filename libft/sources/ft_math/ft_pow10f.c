#include <libft.h>

float	ft_pow10f(int p)
{
	float	out;

	if (p == 0)
		return (1.0f);
	out = 1.0f;
	if (p > 0)
		while (p--)
			out *= 10.0f;
	else
		while (p++)
			out /= 10.0f;
	return (out);
}
