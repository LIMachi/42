#include <ft_printf.h>
#include <ft_int.h>

__uint128_t	cast_uint128(__uint128_t v, int b, int s)
{
	v &= ((__uint128_t)-1) >> (128 - b);
	if (s && v & ((__uint128_t)1 << (b - 1)))
		v |= ((__uint128_t)-1) << b;
	return (v);
}
