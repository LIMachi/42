#include <libft.h>

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*p_tmp;

	if (!n)
		return (NULL);
	p_tmp = (unsigned char *)src;
	while (n--)
		if ((*(unsigned char *)dst++ = *p_tmp++) == (unsigned char)c)
			return ((void *)dst);
	return (NULL);
}
