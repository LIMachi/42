#include <libft.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*p_tmp;
	unsigned int	i;

	p_tmp = (unsigned char *)s;
	i = 0;
	while (i < n && p_tmp[i] != (unsigned char)c)
		++i;
	return (i == n ? NULL : (void *)(s + i));
}
